public class Ej5_OperacionesMatematicas {
    public static void main(String[] args) {
        double numberOne = 2.1;
        double numberTwo = 3;

        System.out.println(Math.ceil(numberOne));
        System.out.println(Math.floor(numberTwo));
        System.out.println(Math.pow(numberOne,numberTwo));
        System.out.println(Math.max(numberOne,numberTwo));
        System.out.println(Math.sqrt(numberTwo));
        System.out.println((4/3)*Math.PI*Math.pow(numberTwo,3));
    }
}
